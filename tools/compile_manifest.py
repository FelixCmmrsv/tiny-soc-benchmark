#!/usr/bin/env python3
"""Compiles a private manifest.source.yaml (plaintext answers, author's own
copy, NEVER distributed) into the distributable manifest.yaml the proctor
actually loads (answers replaced with salted sha256 hashes).

This protects against a third party who receives the harness+scenario
reading the answer straight out of the file -- see grading.py's module
docstring for exactly what this does and doesn't defend against (it does NOT
resist a determined local brute-force search over plausible answers for
low-entropy questions; that trade-off was a deliberate, informed choice, not
an oversight).

Usage:
  python3 harness/tools/compile_manifest.py harness/scenarios/scenario1_ferrumfox
  python3 harness/tools/compile_manifest.py --all
"""
import argparse
import copy
import secrets
import sys
from pathlib import Path

import yaml

HARNESS = Path(__file__).resolve().parent.parent
SCENARIOS_DIR = HARNESS / "scenarios"

sys.path.insert(0, str(HARNESS / "proctor"))
from grading import hash_expected  # noqa: E402


class CompileError(RuntimeError):
    pass


def compile_rule(rule):
    """Returns a new rule dict with 'expected' replaced by 'expected_hash' +
    'salt'. Refuses (raises) on rules that are fundamentally incompatible
    with hash-based grading (see grading.py):

    - tolerance>0: a hash can't do fuzzy/range comparison.
    - shift_from_source: true: the proctor recomputes the correct answer at
      RUNTIME by shifting this value by that run's anchor (a different
      shift every run -- see materialize_shifted_timestamps). Hashing here
      would freeze in the shift computed at COMPILE time, which is never
      the right one for any actual run -- there is no anchor yet to shift
      by when this script runs. This value must stay in plaintext; the
      proctor is still the only thing that ever sees it, same isolation
      guarantee as every other question, just not hash-protected on top.
    """
    rule = copy.deepcopy(rule)
    tolerance = rule.get("tolerance") or rule.get("tolerance_seconds") or 0
    if tolerance:
        raise CompileError(
            "grading rule has tolerance=%s -- fuzzy/range comparison can't be hash-protected "
            "(a hash-based grader can't compare 'within N of the target' without a brute-force "
            "range search revealing the target). Set tolerance to 0 or keep this question in "
            "plaintext mode (leave 'expected' as-is, don't run it through this compiler)." % tolerance
        )
    if rule.get("shift_from_source"):
        raise CompileError(
            "grading rule has shift_from_source: true -- the correct answer is only known at "
            "run time (shifted by that run's anchor), so it can't be pre-hashed at compile time. "
            "Left in plaintext -- this is the one class of question this harness's hashing can't "
            "cover, not a bug to work around."
        )

    if rule.get("expected") is None:
        # unconfirmed/excluded question -- nothing to hash, keep as-is (still ungraded)
        return rule

    salt = secrets.token_hex(16)
    digest = hash_expected(rule["expected"], rule, salt)
    del rule["expected"]
    rule["expected_hash"] = digest
    rule["salt"] = salt
    return rule


def compile_manifest(source_path, dest_path):
    with open(source_path, "r", encoding="utf-8") as f:
        manifest = yaml.safe_load(f)

    left_plaintext = []
    for step in manifest.get("steps", []):
        if "grading" in step:
            try:
                step["grading"] = compile_rule(step["grading"])
            except CompileError as e:
                left_plaintext.append((step.get("n"), str(e)))
    if left_plaintext:
        print("[compile_manifest] WARNING: %d step(s) left in plaintext (can't be hash-protected):"
              % len(left_plaintext))
        for n, reason in left_plaintext:
            print("  step %s: %s" % (n, reason.splitlines()[0]))

    header = (
        "# AUTO-GENERATED by harness/tools/compile_manifest.py from %s -- do not hand-edit.\n"
        "# Answers are salted sha256 hashes, not recoverable from this file.\n"
        "# Re-run the compiler after editing the .source.yaml.\n\n" % source_path.name
    )
    with open(dest_path, "w", encoding="utf-8") as f:
        f.write(header)
        yaml.safe_dump(manifest, f, allow_unicode=True, sort_keys=False, width=100)
    print("[compile_manifest] %s -> %s (%d steps)" % (source_path, dest_path, len(manifest.get("steps", []))))


def find_source_manifests():
    return sorted(SCENARIOS_DIR.glob("*/manifest.source.yaml"))


def main():
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("scenario_dir", nargs="?", help="e.g. harness/scenarios/scenario1_ferrumfox")
    ap.add_argument("--all", action="store_true", help="compile every scenario with a manifest.source.yaml")
    args = ap.parse_args()

    if args.all:
        sources = find_source_manifests()
        if not sources:
            print("No manifest.source.yaml files found under %s" % SCENARIOS_DIR)
        for src in sources:
            compile_manifest(src, src.parent / "manifest.yaml")
        return

    if not args.scenario_dir:
        ap.error("pass a scenario directory or --all")

    scenario_dir = Path(args.scenario_dir)
    src = scenario_dir / "manifest.source.yaml"
    if not src.exists():
        ap.error("no manifest.source.yaml under %s" % scenario_dir)
    compile_manifest(src, scenario_dir / "manifest.yaml")


if __name__ == "__main__":
    main()
