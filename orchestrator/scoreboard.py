"""Renders harness/runs/scoreboard.{json,md} from run summaries -- the
automated replacement for the manual Notion table this benchmark used to be
tracked in. scoreboard.json accumulates every run ever recorded (deduped by
run_id); scoreboard.md renders the latest run per (scenario, model) as a
question x model verdict table, mirroring the original BiZone export layout.
"""
import json
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent.parent
HARNESS = REPO_ROOT / "harness"
SCENARIOS_DIR = HARNESS / "scenarios"
RUNS_DIR = HARNESS / "runs"
SCOREBOARD_JSON = RUNS_DIR / "scoreboard.json"
SCOREBOARD_MD = RUNS_DIR / "scoreboard.md"

VERDICT_SYMBOL = {"correct": "✅", "wrong": "❌", "ungraded": "❓"}


def _load_existing():
    if SCOREBOARD_JSON.exists():
        return json.loads(SCOREBOARD_JSON.read_text())
    return {"runs": []}


def render(new_summaries):
    data = _load_existing()
    by_id = {r["run_id"]: r for r in data["runs"]}
    for s in new_summaries:
        by_id[s["run_id"]] = s  # last write wins if a run_id somehow repeats
    data["runs"] = list(by_id.values())
    RUNS_DIR.mkdir(parents=True, exist_ok=True)
    SCOREBOARD_JSON.write_text(json.dumps(data, indent=2, ensure_ascii=False))

    # latest run per (scenario, model), by duration_seconds insertion order proxy:
    # runs list preserves append order, so last occurrence wins.
    latest = {}
    for r in data["runs"]:
        latest[(r["scenario_id"], r["model"])] = r

    scenarios = sorted(set(k[0] for k in latest))
    lines = ["# Benchmark Scoreboard", "", "Auto-generated by harness/orchestrator/scoreboard.py. Do not edit by hand.", ""]

    for scenario_id in scenarios:
        manifest_path = SCENARIOS_DIR / scenario_id / "manifest.yaml"
        title = scenario_id
        steps = []
        if manifest_path.exists():
            manifest = yaml.safe_load(manifest_path.read_text())
            title = manifest.get("title", scenario_id)
            steps = manifest["steps"]

        models = sorted(m for (s, m) in latest if s == scenario_id)
        lines.append("## %s (`%s`)" % (title, scenario_id))
        lines.append("")
        header = "| # | Question | " + " | ".join(models) + " |"
        sep = "|---|---|" + "---|" * len(models)
        lines.append(header)
        lines.append(sep)

        for step in steps:
            row = ["`%s`" % step["n"], step["question"].replace("|", "\\|")]
            for m in models:
                run = latest[(scenario_id, m)]
                v = run["per_step"].get(str(step["n"])) or run["per_step"].get(step["n"])
                row.append(VERDICT_SYMBOL.get(v, "—"))
            lines.append("| " + " | ".join(row) + " |")

        score_row = ["**Score**", ""]
        cost_row = ["**Usage (in/out/cache_r/cache_w)**", ""]
        for m in models:
            run = latest[(scenario_id, m)]
            score_row.append("**%s**" % run["score"])
            u = run.get("usage", {})
            cost_row.append("%d/%d/%d/%d" % (u.get("input", 0), u.get("output", 0), u.get("cache_read", 0), u.get("cache_write", 0)))
        lines.append("| " + " | ".join(score_row) + " |")
        lines.append("| " + " | ".join(cost_row) + " |")
        lines.append("")

    SCOREBOARD_MD.write_text("\n".join(lines))
    print("[scoreboard] wrote %s and %s" % (SCOREBOARD_MD, SCOREBOARD_JSON))
